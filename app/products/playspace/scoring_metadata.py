"""
Hardcoded scoring metadata for the Playspace PVUA v5.2 instrument.

Values for reverse-scored questions are pre-flipped. The scoring engine
reads addition_value and boost_value directly without reversal logic.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass(frozen=True)
class ScoringScaleOption:
    """One selectable option within a scoring scale."""

    key: str
    addition_value: float
    boost_value: float
    allows_follow_up_scales: bool


@dataclass(frozen=True)
class ScoringScale:
    """One scoring scale for a question."""

    key: str
    options: list[ScoringScaleOption]


@dataclass(frozen=True)
class ScoringQuestion:
    """Per-question scoring metadata."""

    question_key: str
    mode: str
    constructs: list[str]
    domains: list[str]
    scales: list[ScoringScale]


@dataclass(frozen=True)
class ScoringSection:
    """One instrument section with its scoring questions."""

    section_key: str
    questions: list[ScoringQuestion] = field(default_factory=list)


